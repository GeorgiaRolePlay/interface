            document.getElementById("ammo").innerText = `${ammo}`;
            document.getElementById("max_ammo").innerText = `${max_ammo}`;
            document.getElementById("money").innerText = `${money}`;
            document.getElementById("speed").innerText = `${speed}`;
        });
